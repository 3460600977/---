</span>
</p>
<p style="text-align:center;">
	<strong><span style="font-size:15px;">大咖云集，共议2020商业新机遇</span></strong>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">回顾2019，复杂多变的经济环境带来诸多挑战，不确定性中蕴含着时代发展的新机遇。</span><span style="font-size:15px;">值此辞旧迎新之际，川商总会举办2020新年演讲，邀请全球川商一起总结过往、共话未来，共同感知新时代新气象的窗口。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">会上，川商总会会长、新希望集团董事长刘永好分享到，2020年，充满挑战，也充满希望。</span><span style="font-size:15px;">在经济下行，成本上升的大环境下，传统企业要将数字科技、智能化和互联网产业的技术、能力和手段，充分运用到传统企业里，迎接转型期新挑战，确保企业在未来的发展中能够不落后，在新的格局下谋求新的成长与进步。</span><span style="font-size:15px;"></span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<img src="https://cdn.xinchao.com/goods/202001/6e06ba21f95eced9e2d4c8e7c81ee7c31080x720.jpg" alt="" width="900" height="600" title="" align="">
