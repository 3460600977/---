	<span style="font-size:15px;">12月25日，川商总会2020年新年演讲在成都举行。全国工商联副主席、四川省政协副主席、四川省工商联主席陈放，川商总会会长、新希望集团董事长刘永好，川商总会常务副会长、泸州老窖董事长刘淼等嘉宾齐聚盛典，聚焦商业前沿理念，探讨时代发展新趋势。</span>
