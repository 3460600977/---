<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">正如刘永好所说，2020年是充满了挑战的一年。云与数据智能如何帮助企业新升级？</span><span style="font-size:15px;">线下媒体的新赛道在哪里？“流量经济”能走多远？</span><span style="font-size:15px;">川商总会特别邀请到自贡市委副书记、市长何树平，字节跳动副总裁张羽，腾讯云副总裁王龙，新潮传媒集团创始人、董事长张继学，瘾食文化CEO徐林飞五位嘉宾，分别从新角色、新视角、新数据、新赛道、新流量五个维度，就2020商业趋势展开分享。</span><span style="font-size:15px;"></span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
