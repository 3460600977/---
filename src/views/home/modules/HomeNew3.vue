<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"></span><span style="font-size:15px;">张继学说，2020年，新潮传媒将利用60万块电梯智慧屏的流量优势，再帮助超过20个品牌，打造社区经典营销案例，全方位助力品牌与家庭形成链接，让优质产品走进千家万户。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">在智能商业时代的大趋势下，技术已成为企业变革和争夺新赛道的底层驱动力。新潮传媒作为专注中产家庭消费的社区媒体平台，不断推进梯媒数字化发展，加快数媒融合，助力更多品牌打透社区消费，实现“流量+交易”的营销闭环，与众多行业合作伙伴共同成长，携手共建数字营销新生态，共创产业未来。</span>
