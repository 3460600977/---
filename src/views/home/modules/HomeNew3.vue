	<span style="font-size:15px;">新潮传媒集团创始人、董事长张继学应邀出席，并做了《每个行业都值得重做一遍》的主题演讲，解码新潮传媒的数字化进化之路——如何从一家传统媒体升级成为“智能硬件+软件”的梯媒独角兽。</span>
