</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">数字化新时代已来，传统行业都在积极寻求转型升级之路。新潮传媒董事长张继学在新年演讲会上分享，新潮传媒如何利用科技赋能，成为中国线下媒体数字化的变革者。</span>
</p>
<p style="text-align:justify;color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;background-color:#ffffff;"="">
	<span style="font-size:15px;"><br>
</span>
</p>
<p style="text-align:center;">
	<strong><span style="font-size:15px;">解码新潮传媒新赛道：线下媒体数字化</span></strong>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">张继学分享到，数字化时代，新潮传媒在技术赋能下获得发展新动力。在全国100个城市拥有60万块电梯智慧屏，日均覆盖2亿中产家庭人群。2019年10月，胡润研究院发布首份《2019胡润全球独角兽榜》，新潮传媒与蚂蚁金服、字节跳动、滴滴出行、陆金所、爱彼迎、SpaceX等全球知名科技企业共同入榜。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">&nbsp;</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"><img src="https://cdn.xinchao.com/goods/202001/e7ba1656628bde9c015ca54e55a81f411080x718.jpg" alt="" width="900" height="598" title="" align=""><br>
</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"><br>
</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">技术变革正在颠覆传统行业。张继学说，“未来所有行业都将在5G和物联网时代变成智能商业，数据+网络协同将重新定义每一个行业。<span>”</span>线下媒体将进入流量时代。生活空间将会变成物联网流量平台，“万物皆屏，万屏皆媒的时代正式到来。<span>”</span>新潮传媒计划在未来五年内投资100亿元，覆盖10万个社区和1亿个家庭，打造日活3—5亿的线下媒体流量平台，实现电梯媒体的数字化、流量化。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">数字化转型升级的另一层意义是，企业正在进入新营销时代。张继学说，今天的“人、货、场”已经有了新的变化，所以必须制订新的营销策略。针对“人”的策略就是下沉、下沉、再下沉，在下沉市场里“打捞”新市场；针对“货”的策略就是“品牌、品牌、再品牌”，迅速建立品牌影响力，抢占消费者心智；针对这个“场”的策略就是“社区、社区、再社区”，离社区越近的地方越有价值。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"><br>
</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">新潮传媒一直精耕社区。在不久前举行的“智者•思变”2020探索大会上，</span><span style="font-size:15px;">新潮传媒揭晓了2019中国社区经典营销案例，顾家家居、云南白药、<span>国美、</span>立白、思念食品、太太乐等24个品牌，展示了它们在数字化梯媒时代出色的营销力和影响力。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
