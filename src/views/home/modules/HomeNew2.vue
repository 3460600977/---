	<p>
		<span style="font-size:15px;"> </span><span style="font-size:15px;">5年内，线上和线下将会融合成一张网，数字化的生活空间，如电梯，会变成5G时代的流量APP。</span><span style="font-size:15px;">置身于</span><span style="font-size:15px;">线下媒体数字化技术大变革中，新潮传媒通过线上线下融合，技术和数据赋能，开启了社区营销新时代。</span><span style="font-size:15px;">张继学分享到，在新技术的加持下，新潮传媒不断探索数字营销，用“互联网+科技”重新定义电梯媒体，创造出电梯媒体数字化、流量化的新物种。</span><span style="font-size:15px;">新潮传媒已和众多品牌达成合作，新潮传媒助力百度，打造中国第一个线上+线下融合的流量平台；</span><span style="font-size:15px;">帮助京东线下+线上数据打通，形成交易流量闭环。</span> 
