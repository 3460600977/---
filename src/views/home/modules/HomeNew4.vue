</p>
<p style="text-align:center;">
	<strong>媒体流量变革的新时代，流量决定存亡</strong>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">&nbsp;</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">无论处于什么时代，媒体最基本的作用就是广告，而广告本身的价值，在于受众的消费力。</span><span style="font-size:15px;">媒体覆盖的流量越多、消费能力越强，广告看的越仔细，广告的价值就会越高。</span><span style="font-size:15px;">梁春华认为，流量这个词本来是互联网广告的统计维度，但是它代表的意义在户外媒体上也是适用的，最终影响到的还是媒体覆盖的人。</span><span style="font-size:15px;">因为流量本身的变化，所以媒体的影响力也分不同时代。</span><span style="font-size:15px;">&nbsp;</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"><br>
</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"><img src="https://cdn.xinchao.com/goods/201912/07fdb91c942576738a5785b29edb140e1080x720.jpg" alt="" width="900" height="600" title="" align=""><br>
</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
<span style="font-size:15px;"></span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">用电商的逻辑来看，1.0时代，是小广告电话订购时代；</span><span style="font-size:15px;">2.0时代，出现了电商雏形，大家买东西开始需要搜索和比价，这个颗粒度变小了；</span><span style="font-size:15px;">3.0时代，电商时代，流量越来越贵，货却越来越多；</span><span style="font-size:15px;">4.0电商，电器变网器，家里的冰箱安上传感器、摄像头，交付功能之后，电商可能开始预测需求，制造需求并满足这个需求。</span><span style="font-size:15px;">这样，整个消费生态的效率、成本都能朝着更优的方向发展。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">&nbsp;</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">5年内， 线上线下会融合成一张网，数字化的生活空间，会变成5G时代的流量APP。</span><span style="font-size:15px;">未来媒体的价值，流量的量不再是单一的衡量线，流量智能运营的能力将成为媒体价值的关键。</span><span style="font-size:15px;">这样，媒体行业也会更加融入到营销的这个圈里，是结合智能终端+综合运营结合在一起的新型服务。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">&nbsp;</span>
</p>
<p style="text-align:center;">
	<strong><span>智能时代的社区梯媒，为创新营销赋能</span><span></span></strong>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
<span style="font-size:15px;"></span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">梯媒已成为互联网巨头完成新零售产业链的又一发力点。</span><span style="font-size:15px;">就目前形势来看，梯媒成为了线下流量红利，社区梯媒成新营销入口。</span><span style="font-size:15px;">而5G和物联网将把电梯广告，从手工时代升级为流量时代。</span><span style="font-size:15px;">置身于线下媒体数字化技术大变革中，梯媒通过线上线下融合，技术和数据赋能，已经开启了千楼千面的社区消费新时代。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	&nbsp;
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<img src="https://cdn.xinchao.com/goods/201912/4b32f68cbbde1d1edc8735d4b5ebe0a61080x720.jpg" alt="" width="900" height="600" title="" align="">
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<br>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"></span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">新潮传媒在建立之初，就以梯媒的数字化、智能化为目标，并且一直在用技术为新零售赋能。</span><span style="font-size:15px;">与百度达成战略合作，联手打造<span style="color:#252323;font-family:" "="">“AI+梯媒”</span>的线上+线下融合的流量平台；</span><span style="font-size:15px;">与京东合作，借助大数据、AIoT等技术，实现线下广告“人、货、场、圈”的精准匹配，提升线下广告的触达效果；</span><span style="font-size:15px;">与华为、中移物联、信大捷安共同成立“安全、智能、精准”的生活圈物联网流量实验室；</span><span style="font-size:15px;">与中国联通、华为、百度、信大捷安、康佳共同发布中国首个5G电梯安全及智慧媒体平台。</span><span style="font-size:15px;"></span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;"><br>
</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">新潮传媒已经将这些技术广泛运用在新零售中。</span><span style="font-size:15px;">燕之屋通过新潮电梯智慧屏，实现了618和双11两个重要营销节点均成为滋补品类销量冠军，投放后的站内店铺词搜索量的提升高达149％；</span><span style="font-size:15px;">新潮助力鹿客行业奇袭，投放期内百度指数显示品牌词搜索同比增长262%，移动搜索同比增长309%，且618鹿客销量达到1.26亿；</span><span style="font-size:15px;">顾家家居在与新潮深度合作的过程中，在全球布局了超过6000家品牌门店，今年双十一当天的全渠道零售录单总额更是达到7.51亿元，再创历史新高……在2019年BRANDZ最具价值中国品牌100强中，已经有71个品牌与新潮建立合作关系。</span>
</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
