</p>
<p style="color:#333333;font-family:-apple-system-font, BlinkMacSystemFont, " font-size:17px;text-align:justify;background-color:#ffffff;"="">
	<span style="font-size:15px;">新潮传媒凭借“鹿客智能锁线下电梯智慧屏营销”，成功斩获“年度创新营销行业大奖——数码家居金奖”。</span><span style="font-size:15px;">新潮传媒集团合伙人、高级副总裁梁春华受邀出席，带来了《5G时代下，数字化梯媒如何为创新营销赋能》的主题演讲，与参会嘉宾一起，对新营销进行了深入的探讨和分享。</span>
</p>
