	<span style="font-size:15px;">12月19日，2019中国创新营销峰会暨中国创新营销大奖颁奖典礼在北京举行。</span><span style="font-size:15px;">本次峰会以Smart+为主题，聚焦前沿营销技术、新零售、数字化转型等议题，捕捉行业趋势，精准输出心智。</span>
</p>
